import { NgModule } from '@angular/core';
import { RouterModule, Routes } from '@angular/router';
import { CartPageModule } from '../cart/cart.module';
import { NearByPageModule } from '../near-by/near-by.module';
import { ProfilePageModule } from '../profile/profile.module';
import { TabsPage } from './tabs.page';

const routes: Routes = [
  {
    path: '',
    component: TabsPage,
    children: [
      {
        path: 'tab1',
        loadChildren: () => import('../home/home.module').then(m => m.HomePageModule)
      },
      {
        path: 'tab2',
        loadChildren: () => import('../near-by/near-by.module').then(m => NearByPageModule)
      },
      {
        path: 'tab3',
        loadChildren: () => import('../cart/cart.module').then(m => CartPageModule)
      },
      {
        path: 'tab4',
        loadChildren: () => import('../profile/profile.module').then(m => ProfilePageModule)
      },
      {
        path: '',
        redirectTo: '/tabs/tab1',
        pathMatch: 'full'
      }
    ]
  },
  {
    path: '',
    redirectTo: '/tabs/tab1',
    pathMatch: 'full'
  }
];

@NgModule({
  imports: [RouterModule.forChild(routes)],
})
export class TabsPageRoutingModule { }
